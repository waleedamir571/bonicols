                                                    </li>
                                                    <li>Tax refund interception
                                                    </li>
                                                    <li>Driver’s and professional license suspension
                                                    </li>
                                                    <li>Property liens or bank account seizures
                                                    </li>
                                                    <li>Contempt of court (fines or even jail time in serious cases)
                                                    </li>

                                                </ul>

