                        <h3>What Does Child Support Arrears Mean? </h3>
                                              <div class="my-texttt">
                                                <div class="text">Child support refers to the periodic payment that the co-parent has to pay after the termination of marriage or divorce.
                                                The court judge orders non-custodial parents to pay a certain amount in order to fulfill the child's needs, including food, clothing, education, shelter, health, and medical care. 
                                                      <br><br>
                                                      When there is a joint custody case, the child has two custodial parents and no non-custodial parent. In this case, the custodial parent who earns higher income is obliged to pay the other custodial parent with less income. 
                                                      <br><br>
                                                      The judge decides the child support amount by calculating a certain percentage of net income. It depends on the circumstances of the parent, number of children, joint custody, or split custody. 
                                                      
                                                      <br><br>
                                                      In some cases, child support is paid directly by the non-custodial parent to the custodial parent, while in other cases, the co-parent pays the amount to the state. Paying child maintenance 
                                                      or child support as per the order is the legal responsibility of the parent, and they may face the consequences if they do not pay the amount. 
                                                      <br><br>
                                                      Child support arrears mean that the non-custodial parent lags in paying the full amount or does not 
                                                      pay for child support. There may be some valid reasons for not paying the amount, including lack of finances, job insecurity, or conflict with another parent. 
                                                      <br><br>
                                                      There are 2 main types of child support arrears: 
                                                
                                             
</div>
    </div>
                  <h3>Assigned Arrears</h3>
                                              <div class="my-texttt">
                                                <div class="text">In assigned arrears, the custodial parent is on government assistance, and the state will compensate for the unpaid payments. That means the custodial parent gets the financial aid from the state in the absence of child support 
                                                and must pay back when debts are received from the non-custodial parent. Also, in this case, the non-custodial parent can pay child support to the state rather than to the co-parent. 
                                             
</div>
    </div>
        <h3>Unassigned Arrears</h3>
                                              <div class="my-texttt">
                                                <div class="text">If the custodial parent is not on government assistance, the non-custodial parent has to pay child support directly
                                                to the custodial parent. If the payment is missed, it will add on together – known as back child support until the non-custodial parent starts paying child support. 
                                                      <br><br>
                                                     As per the Texas Family Code (157.261), the state allows interest to be charged at a rate of 6% per year from the date of a missed payment.  
    </div>
                                    </div>
                                         <h3>What If The Non-Custodial Parent Is In Child Support Arrears?</h3>
                                              <div class="my-texttt">
                                                <div class="text">If your ex-spouse fails to pay child support, you are permitted to get back child support. If you both have good terms, the best advice is to discuss the matter and find out 
                                                the reasons for not paying child support. No matter it is hard for you to speak with your co-parent, it is a good idea to communicate honestly about the problems you're facing.  
                                                
                                                
                                                
                                                      <br>
                                                    If this is not the case, you can contact the Office of the Attorney General for enforcement. 
                                                      <br>
                                                      
                                                   If the non-custodial parent has good reasons for not paying the child support, they can speak to the custodial parent to work with them. Also, if the financial
                                                   conditions are not good, they can petition the court to modify the court order, but this will only work if the non-custodial parent can prove that they cannot pay the amount. 
                                                   <br><br>
                                                   Also, note that the misrepresentation of financial situation and not paying child support can have serious consequences, including: 
                                                   <br>
                                                   <ul >
                                                <li style="list-style: disc !important;">The court may withhold your wages.</li>
                                                  <li style="list-style: disc !important;">Withhold or take a part of your employment benefits.</li>
                                                    <li style="list-style: disc !important;">Suspend your driving license.</li>
                                                      <li style="list-style: disc !important;">May charge you with a violation if you can pay but do not.</li>
                                                      <li style="list-style: disc !important;">Take your refund on state taxes.</li>
                                                       <li style="list-style: disc !important;">Minimize the state benefits that you are receiving. </li>
                                                      <li style="list-style: disc !important;">The state can deny a new or renewed passport. </li>
                                                      <li style="list-style: disc !important;">They can file a lien on bank accounts, properties, insurance, retirement plans, and awards if the parent does not pay child support. </li>
    
                                            </ul>
    </div>
                                    </div>
                                               <h3>Can Custodial Parent Forgive Child Support Arrears? </h3>
                                              <div class="my-texttt">
                                                <div class="text">The custodial parent can forgive child support payment and submit a waiver to the court that dismisses non-custodial parent back child support. 
                                                However, it is uncommon and only happens if the parents reunite, or the custodial parent does not need money to support the child. 
                                                
                                                
                                                
                                                      <br>
                                                      
                                                   If the custodial parent does not forgive back child support, the debt remains, and the non-custodial parent has to pay back child support. 
                                    </div>
                                    
                                    
                                    
