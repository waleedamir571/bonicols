                                        What Is Child Support?
                                    </h3>

                                    <div class="my-texttt">
                                        <div class="text"><a href="https://bonicholslaw.com/child-support">Child
                                                support</a> is the legally enforceable obligation of a
                                            non-custodial parent to assist financially

                                            with the upbringing of their child. It’s not a gift or optional payment—it’s
                                            a right of the child
                                            and a duty of the parent. The goal is to ensure that children continue to
                                            receive the financial
                                            benefit of both parents, even if those parents live apart.
                                            <br>
                                            Typically, <a href="https://bonicholslaw.com/child-support">child
                                                support</a> payments cover essential aspects of a child’s
                                            needs: food, shelter,
                                            clothing, education, medical care, and other regular expenses. While the
                                            custodial parent often
                                            handles day-to-day costs, the non-custodial parent’s contributions help
                                            bridge the gap and
                                            promote balance.

