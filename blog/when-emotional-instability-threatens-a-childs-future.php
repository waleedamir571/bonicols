                                                <h3>
                                                    Common Misconceptions About Child Support
                                                </h3>
                                                
                                                <div class="text">
                                                    <b>“Once support is set, it can’t change.”</b>
                                                    <br>

                                                    That’s false. Courts allow modifications when significant
                                                    circumstances change—such as a
                                                    parent’s income, custody schedule, or the child’s needs.
                                                    <br>
                                                    <b>“Child support only covers basic needs.”</b>
                                                    <br>

                                                    Support typically includes health insurance premiums,
                                                    extracurricular activities, childcare,
                                                    tutoring, and other related expenses—not just food and clothing.
                                                    <br>
                                                    <b>“If the custodial parent remarries, support decreases.”</b>
                                                    <br>

                                                    Not necessarily. The court bases its support on the child’s needs
                                                    and the parents’ financial
                                                    abilities, not the custodial parent’s new household income.
                                                    <br>
                                                    <br>
                                                    <b>“Missing payments is just a financial issue.”</b>
                                                    <br>

                                                    Nonpayment can trigger serious consequences: wage garnishment, tax
                                                    refund interception,
                                                    driver’s license suspension, contempt of court, or even jail time.
                                                    <br>
                                                    Understanding these myths helps parents make informed, responsible
                                                    decisions about child
                                                    support.

                                                </div>

