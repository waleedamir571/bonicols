                                    <h3>
                                        The Family Home


                                    </h3>

                                    <div class="my-texttt">
                                        <div class="text">Courts consider who contributed financially, who will have
                                            primary custody of the children, and whether selling the home is in both
                                            parties’ best interest. Options include:

                                            <br>
                                            One spouse buys out the other.
                                            <br>
                                            Both spouses sell and split the proceeds.
                                            <br>
                                            Temporary possession for the spouse with children


                                        </div>
                                    </div>
                                    <h3>
                                        Retirement & Investments

                                    </h3>

                                    <div class="my-texttt">
                                        <div class="text">Retirement savings earned during marriage are community
                                            property — even if only one spouse contributed. Courts may use a Qualified
                                            Domestic Relations Order (QDRO) to divide 401(k)s or pensions.
                                        </div>
                                    </div>
                                    <h3>
                                        Business Ownership

                                    </h3>

